// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:     "pharos",
	Example: "pharos new <project-name>",
	Version: "0.0.1",
}

func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

